package vo;

public class FoodVo {
	
	int f_idx;
	String f_name;
	int f_unit_g;
	double f_unit_kcal;
	double f_csum_g;
	String animal_plant;
	
	public int getF_idx() {
		return f_idx;
	}
	public void setF_idx(int f_idx) {
		this.f_idx = f_idx;
	}
	public String getF_name() {
		return f_name;
	}
	public void setF_name(String f_name) {
		this.f_name = f_name;
	}
	public int getF_unit_g() {
		return f_unit_g;
	}
	public void setF_unit_g(int f_unit_g) {
		this.f_unit_g = f_unit_g;
	}
	public double getF_unit_kcal() {
		return f_unit_kcal;
	}
	public void setF_unit_kcal(double f_unit_kcal) {
		this.f_unit_kcal = f_unit_kcal;
	}
	public double getF_csum_g() {
		return f_csum_g;
	}
	public void setF_csum_g(double f_csum_g) {
		this.f_csum_g = f_csum_g;
	}
	public String getAnimal_plant() {
		return animal_plant;
	}
	public void setAnimal_plant(String animal_plant) {
		this.animal_plant = animal_plant;
	}
	
	

}
